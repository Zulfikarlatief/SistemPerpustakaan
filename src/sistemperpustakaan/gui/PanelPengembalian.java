        for(PeminjamanModel pm :listPinjam){
            if(jTextField1.getText().equals(Integer.toString(pm.getNo()))){
                jTextField3.setText(pm.getNama());
                jTextField2.setText(pm.getBuku());
                jTextField5.setText(format.format(pm.getTglPinjam()));
                jTextField6.setText(format.format(pm.getTglKembali()));
                if(current.after(pm.getTglKembali()))
                    jTextField4.setText("5000");
                else
                    jTextField4.setText("0");
                
            } 
        }
