    private void isiTabel(){
        no=0;
        format = new SimpleDateFormat("yyyy-MM-dd");
        DefaultTableModel dtm = new DefaultTableModel(new Object[][]{},new String[]{"No","NoPinjam","Tgl Kembali","Denda"});
        for(PengembalianModel pm:list){
            ++no;
            dtm.addRow(new Object[]{
                pm.getNoKembali(),pm.getNoPinjam()
                    ,String.valueOf(format.format(pm.getTglKembali())),pm.getDenda()
            });
            
        }
        jTable1.setModel(dtm);
    }
    
    private void simpan(){
        PengembalianModel pm = new PengembalianModel();
        format = new SimpleDateFormat("yyyy-MM-dd");

        pm.setNoKembali(++no);
        pm.setNoPinjam(Integer.parseInt(jTextField1.getText()));
        pm.setTglKembali(current);        
        pm.setDenda(Integer.parseInt(jTextField4.getText()));
        try {
            db.Input(pm);
        } catch (SQLException e) {
            System.out.println("Input db error "+e.getMessage());
        }
    }
    
    private void hapus(){
        format = new SimpleDateFormat("yyyy-MM-dd");
        PengembalianModel pm = new PengembalianModel();
         if(jTable1.getSelectedRow()!=-1){
               pm.setNoKembali(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString()));
               pm.setNoPinjam(Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 1).toString()));
               pm.setDenda(Long.parseLong(jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString()));
               try {
                    pm.setTglKembali(format.parse(jTable1.getValueAt(jTable1.getSelectedRow(), 2).toString()));        
            
             } catch (ParseException e) {
                    System.out.println("Parsing tanggal gagal "+e.getMessage());
             }
             }
        try {
            db.Hapus(pm);
        } catch (SQLException e) {
            System.out.println("Hapus db error"+e.getMessage());
        }
    }
    
